public class MathIndexLogarithmic {
    public static void main(String[] args) {
        System.out.println("5的立方根是：" + Math.cbrt(5));
        System.out.println("e的3次方是：" + Math.exp(3));
        System.out.println("e的6次方是：" + Math.expm1(6));
        System.out.println("5的自然对数是：" + Math.log(5));
        System.out.println("8的10为底的对数是：" + Math.log10(8));
        System.out.println("7的自然对数是：" + Math.log1p(7));
        System.out.println("2的3次方是：" + Math.pow(2, 3));
        System.out.println("4的平方根是：" + Math.sqrt(4));
    }
}